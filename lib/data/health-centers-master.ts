// 헬퍼 함수들
export function getHealthCentersByRegion(region: string): string[] {
  const regionData = HEALTH_CENTERS_BY_REGION.find(r => r.region === region);
  return regionData ? [...regionData.centers, '기타 (직접 입력)'] : ['기타 (직접 입력)'];
}

export function getAllHealthCenters(): string[] {
  const centers: string[] = [];
  HEALTH_CENTERS_BY_REGION.forEach(region => {
    centers.push(...region.centers);
  });
  return centers;
}

// 보건소명 정규화 함수 (데이터베이스 매칭용)
export function normalizeHealthCenterName(name: string): string {
  return name
    .replace(/\s+/g, '') // 공백 제거
    .replace(/특별시|광역시|특별자치시|특별자치도/g, '') // 시도 접미사 제거
    .replace(/보건소$/g, '') // '보건소' 접미사 제거 (필요시)
    .toLowerCase();
}

// 두 보건소명이 같은지 비교
export function isHealthCenterMatch(name1: string, name2: string): boolean {
  return normalizeHealthCenterName(name1) === normalizeHealthCenterName(name2);
}